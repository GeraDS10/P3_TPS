package Ejercicio_3;

public class Actividad {
    int horaComienzo;
    int horaFin;

    public Actividad(int horaComienzo, int horaFin) {
        this.horaComienzo = horaComienzo;
        this.horaFin = horaFin;
    }

    public int getHoraComienzo() {
        return horaComienzo;
    }

    public int getHoraFin() {
        return horaFin;
    }
}
